# Ejemplo de iterar sobre items
frutas = ['manzana','pera','limon']
for fruta in frutas:
     print("La palabra", fruta, "tiene", len(fruta), "letras")


# Ejemplo de iterar sobre frecuencias de numeros
for i in range(10):
    print("Imprime del 0 al 9 ",i)

for i in range(5, 10):
    print("Imprime de 5 a 9", i)

for i in range(0, 10, 3):
    print("Imprime de 0 a 9, de a tres unidades", i)    